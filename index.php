              <li>
                <div data-bs-toggle="collapse" class="collapsed question" href="#faq1">¿Qué coste tendrá mi app? <i
                    class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq1" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Esta quizás sea la pregunta estrella, pero es más fácil plantearla que responderla sobre todo si no
                    se cuenta con la información suficiente. Imaginemos que alguien entra en un concesionario de coches
                    y dice que quiere presupuesto de un coche de 4 puertas. Aunque el comercial puede asesorarle, no
                    puede darle un precio con esos datos porque el estilo del vehículo y sus extras determinarán el
                    coste final. Pues de la misma forma pasa con las aplicaciones hechas a medida, solo que para bien o
                    para mal no están expuestas detrás de una vitrina esperando a ser elegidas por el cliente por lo que
                    el abanico de complejidad es amplísimo y solo se va cercando a medida que se tiene más información
                    sobre la aplicación a desarrollar.

                    Por lo tanto, para conocer el precio de una aplicación el equipo técnico necesita recibir un
                    documento que contenga su finalidad, la descripción funcional e incluso las vistas que contendrá. En
                    el caso de que no se haya hecho previamente este trabajo, será necesaria una labor de consultoría
                    para ayudar al cliente a definir su app antes de poder dar un presupuesto.
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq2" class="collapsed question">¿Qué tiempo llevará
