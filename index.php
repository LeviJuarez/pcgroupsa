                <div id="faq4" class="collapse" data-bs-parent=".faq-list">
                  <p>

                    1. Estudio de la funcionalidad: revisión de la documentación aportada por el cliente. <br>
                    2. Reunión con el cliente para aclarar dudas y validar conjuntamente el proyecto final.<br>
                    3. Exponer al cliente la solución propuesta, planificación y presupuesto.<br>
                    4. Diseño UX y validación por parte del cliente.<br>
                    5. Diseño UI<br>
                    6. Desarrollo app Android / Desarrollo app iOS<br>

                  </p>
                </div>
              </li>

