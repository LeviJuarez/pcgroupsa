                </div>
                <div id="faq2" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Esta cuestión está relacionada con la anterior porque dependiendo de la envergadura de la
                    aplicación, el esfuerzo en el desarrollo de apps será mayor o menor. Para hacernos una idea,
                    podríamos decir que una app sencilla necesitaría en cada plataforma de un mes y medio de trabajo,
                    una de complejidad media entre 3 y 5 meses y las de complejidad elevada de 6 meses en adelante.
                  </p>
                </div>
              </li>

