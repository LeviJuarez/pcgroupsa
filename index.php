                <div id="faq3" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    La empresa de desarrollo de apps necesita entender al cliente, qué problema resuelva su aplicación,
                    cuál es su objetivo, de dónde se obtienen los datos que ofrece la app, qué monetización tiene
                    pensada para su app, cuál es su modelo de negocio. Todos estos aspectos nos ayudan a ponernos en la
                    piel del cliente, esto no va solo de funcionalidades.

                    Lo ideal series que el cliente haya trabajado previamente la idea de su app, redactando un briefing
                    que contenga las funcionalidades de su aplicación móvil. Si indica la prioridad de las mismas, se
                    entenderá mejor cuál es el core de la app y facilitará obtener un MVP o pasear el proyecto en el
                    caso de que sea necesario.


                  </p>
