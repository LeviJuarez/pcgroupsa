                    class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq5" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    El backend, también llamado CMS o servidor de la aplicación móvil centraliza las peticiones que se
                    hacen desde el dispositivo móvil. Así, cuando el usuario hace una petición en la app, el backend
                    accede a su repositorio de datos, encuentra y filtra la información solicitada y la devuelve al
                    usuario.

                    Hay que tener en cuenta que la mayoría de los puestos en el desarrollo de apps se necesita un
                    servidor, y su desarrollo tiene la inversión como mínimo de una aplicación sencilla. En ocasiones
                    hay empresas que ya tienen funcionando un CMS que utilizará la aplicación móvil para obtener los
                    datos, para el resto de casos nosotros nos encargamos del desarrollo del servidor.


                  </p>
                </div>
